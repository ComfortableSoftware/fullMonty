FILE_POPENARGSTUP = (
	"/usr/bin/file -i '&F&'",  # POPENARGS entry for 'file' command
)

FILETUP = (
	(CAPOUT, True),  # capture output
	(SHELL, True),  # use a normal shell to execute this command
	(TEXT, True),  # return text rather than bytes
)

def FILEDICT(fileName_):
	FILE_PARMS = [
		("&F&", fileName_),
	]

	dictToRtn_ = dict((x, y) for x, y in FILETUP)
	listToRtn_ = list((x) for x in FILE_POPENARGSTUP)
	listToRtn_ = CF.subParms(listToRtn_, FILE_PARMS)
	return listToRtn_, dictToRtn_


